// Function to select and lock-in language
function setLanguage(langCode) {
    localStorage.setItem('selectedLanguage', langCode);
}

// Function to run on DOMContentLoaded on every page
function applyTranslations() {
    const currentLang = localStorage.getItem('selectedLanguage') || 'en';

    // Find all components with a translation key tag
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const translationKey = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][translationKey]) {

            // Special treatment if the element is a select dropdown dropdown-menu-select wrapper
            if (element.tagName === 'OPTION' && element.disabled) {
                element.textContent = translations[currentLang][translationKey];
            } else if (element.innerHTML.includes('<br>') || translations[currentLang][translationKey].includes('<br>')) {
                // Allows formatting parameters for string elements like line breaks in the queue layout
                element.innerHTML = translations[currentLang][translationKey];
            } else {
                element.textContent = translations[currentLang][translationKey];
            }
        }
    });
}

// Auto run on load if window context is standard subpage
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
    applyTranslations();
}